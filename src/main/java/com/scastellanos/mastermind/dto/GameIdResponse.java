package com.scastellanos.mastermind.dto;


/**
 * Response class wrapper to hold a game ID
 */
public class GameIdResponse {

	private Long gameId;

	/**
	 * @return the gameId
	 */
	public Long getGameId() {
		return gameId;
	}

	/**
	 * @param gameId the gameId to set
	 */
	public void setGameId(Long gameId) {
		this.gameId = gameId;
	}
	

}
